#include "libft.h"

int		ft_atoi(char const *str)
{
	int i;
	int d;
	int p;

	i = 0;
	p = 0;
	d = 0;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\v' || \
			str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			p = 1;
		i++;
	}
	while ((str[i] >= '0' && str[i] <= '9'))
	{
		d = d * 10 + str[i] - 48;
		i++;
	}
	if (p == 1)
		d = d * -1;
	return (d);
}
