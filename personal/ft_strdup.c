#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*cpy;
	int		len;
	int		i;

	len = ft_strlen(src);
	i = 0;
	cpy = (char*)malloc(sizeof(*src) * (len + 1));
	if (cpy == NULL)
		return (NULL);
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
